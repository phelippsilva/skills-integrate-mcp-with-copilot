"""
High School Management System API

A super simple FastAPI application that allows students to view and sign up
for extracurricular activities at Mergington High School.
"""

from fastapi import FastAPI, HTTPException, Depends
from fastapi.staticfiles import StaticFiles
from fastapi.responses import RedirectResponse
from fastapi.security import HTTPBearer, HTTPAuthorizationCredentials
from pydantic import BaseModel
import os
import json
import uuid
from pathlib import Path

app = FastAPI(title="Mergington High School API",
              description="API for viewing and signing up for extracurricular activities")

# Mount the static files directory
current_dir = Path(__file__).parent
app.mount("/static", StaticFiles(directory=os.path.join(Path(__file__).parent,
          "static")), name="static")

# Load teacher credentials from JSON file
teachers_file = os.path.join(current_dir, "teachers.json")
with open(teachers_file) as f:
    teachers_data = json.load(f)
teachers = {t["username"]: t["password"] for t in teachers_data["teachers"]}

# In-memory session store: token -> username
active_sessions: dict[str, str] = {}

security = HTTPBearer(auto_error=False)


class LoginRequest(BaseModel):
    username: str
    password: str


def get_current_teacher(credentials: HTTPAuthorizationCredentials = Depends(security)):
    """Dependency that validates the bearer token and returns the teacher username."""
    if credentials is None or credentials.credentials not in active_sessions:
        raise HTTPException(status_code=401, detail="Not authenticated")
    return active_sessions[credentials.credentials]

